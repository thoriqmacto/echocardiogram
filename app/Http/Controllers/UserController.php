            return DataTables::of($data)
                ->addIndexColumn()
                ->addColumn('action', function ($row) {
                    $btn = '<a href="javascript:void(0)" class="btn btn-info btn-sm">Edit</a>';
                    return $btn;
                })
                ->rawColumns(['action'])
